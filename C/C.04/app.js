function esParOInpar (numeroUsuario) {
    numero = parseInt(numeroUsuario)
    if (numero % 2 == 0) {
        console.log(`El numero ${numeroUsuario} es par`)
    } else {
        console.log(`El numero ${numeroUsuario} es inpar`)
    }
}

